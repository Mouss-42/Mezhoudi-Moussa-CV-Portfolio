let currentLang = 'fr';

function updateContent(lang) {
    currentLang = lang;
    const t = translations[lang];

    document.title = t.title;
    document.getElementById('title').textContent = t.title;
    document.getElementById('presentation-text').textContent = t.presentationText;
    document.getElementById('experience-text').textContent = t.experienceText;
    document.getElementById('goals-text').textContent = t.goalsText;
    document.getElementById('passions-text').textContent = t.passionsText;
    document.getElementById('contact-title').textContent = t.contact;
    document.getElementById('footer-text').textContent = t.footerText;

    document.querySelectorAll('[data-translate]').forEach(el => {
        const key = el.getAttribute('data-translate');
        if (t[key]) {
            el.textContent = t[key];
        }
    });

    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.toggle('active', btn.dataset.lang === lang);
    });

    addTypewriterEffect(document.getElementById('title'), t.title);
}

function addTypewriterEffect(element, text, speed = 50) {
    let i = 0;
    element.innerHTML = '';
    function typeWriter() {
        if (i < text.length) {
            element.innerHTML += text.charAt(i);
            i++;
            setTimeout(typeWriter, speed);
        }
    }
    typeWriter();
}

function addHoverEffect() {
    document.querySelectorAll('.timeline-content').forEach(item => {
        item.addEventListener('mouseenter', () => {
            item.style.transform = 'scale(1.05)';
            item.style.transition = 'transform 0.3s ease';
        });
        item.addEventListener('mouseleave', () => {
            item.style.transform = 'scale(1)';
        });
    });
}

function addSmoothScrolling() {
    document.querySelectorAll('a[href^="#"]').forEach(anchor => {
        anchor.addEventListener('click', function (e) {
            e.preventDefault();
            document.querySelector(this.getAttribute('href')).scrollIntoView({
                behavior: 'smooth'
            });
        });
    });
}

document.addEventListener('DOMContentLoaded', () => {
    updateContent(currentLang);

    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.addEventListener('click', () => {
            updateContent(btn.dataset.lang);
        });
    });

    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.classList.add('animate');
            }
        });
    }, { threshold: 0.1 });

    document.querySelectorAll('.timeline-item').forEach(item => {
        observer.observe(item);
    });

    addHoverEffect();
    addSmoothScrolling();
});